#include<stdio.h>

int main(){
    /* escape sequence = a character combination consisting of a backslash \
                         followed by a letter or combination of digits
                         They specify actions within a line or a string of text.
                         \n = new line
                         \t = tab
    */

   printf("Hail\nto\nthe\nmotherland");
   
   printf("\n1\t2\t3\n4\t5\t6\n7\t8\t9\n");

   printf("\"I love Russia\" - Stalin");

   return 0;
}